    private void tbdataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbdataMouseClicked
        // TODO add your handling code here:
        int baris = tbdata.rowAtPoint(evt.getPoint());
            String kode = tbdata.getValueAt(baris, 1).toString();
            txt_kode.setText(kode);
            String nama = tbdata.getValueAt(baris, 2).toString();
            txt_nama.setText(nama);
            String jumlah = tbdata.getValueAt(baris, 3).toString();
            txt_jml.setText(jumlah);
            String jenis = tbdata.getValueAt(baris, 4).toString();
            box_jenis.setSelectedItem(jenis);
            
            String note = tbdata.getValueAt(baris, 6).toString();
            txt_note.setText(note);
            
    }//GEN-LAST:event_tbdataMouseClicked

