    private void btn_submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_submitMouseClicked
        // TODO add your handling code here:
        String in_out;
        if (rb_in.isSelected()){
            in_out = "Masuk";
        }else{
            in_out = "Keluar";
        }
        try{
                String sql = "INSERT INTO tb_sipro1(kode, nama, jumlah, jenis, in_out, note) VALUES (" + "'" + txt_kode.getText() + "','" + txt_nama.getText() + "','" 
                + txt_jml.getText() + "','" + box_jenis.getSelectedItem() + "','" + in_out + "','" + txt_note.getText() + "')";
                java.sql.Connection conn=(java.sql.Connection)project.Koneksi.getConnection();
                java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data anda berhasil disimpan");
                TampilData();
                Clean();  
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null, "Gagal disimpan");
                System.out.println(e.getMessage());
            }
    }//GEN-LAST:event_btn_submitMouseClicked

