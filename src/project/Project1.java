    private void cariData(String key){


DftTblModel_barang = new DefaultTableModel();
        DftTblModel_barang.addColumn("Tanggal");
        DftTblModel_barang.addColumn("Kode Barang");
        DftTblModel_barang.addColumn("Nama Barang");
        DftTblModel_barang.addColumn("Jumlah Barang");
        DftTblModel_barang.addColumn("Jenis Barang");
        DftTblModel_barang.addColumn("Keluar/Masuk");
        DftTblModel_barang.addColumn("Note");
        tbdata.setModel(DftTblModel_barang);
        Connection conn = Koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "SELECT * FROM tb_sipro1 WHERE kode LIKE '%"+key+"%' OR nama LIKE '%"+key+"%' OR jenis LIKE '%"+key+"%'";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_barang.addRow(new Object[]{
                    res.getString("tanggal"),
                    res.getString("kode"),
                    res.getString("nama"),
                    res.getString("jumlah"),
                    res.getString("jenis"),
                    res.getString("in_out"),
                    res.getString("note")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
    
