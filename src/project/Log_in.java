    private void btn_regMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_regMouseClicked
        // TODO add your handling code here:
        String id = tx_id.getText().toString().trim();
        String nama = tx_nama.getText().toString().trim();
        String jabt = tx_jabatan.getText().toString().trim();
        String pw1 = tx_pw.getText().toString().trim();
        String pw2 = tx_pw2.getText().toString().trim();
        
        if(!pw1.equals(pw2)){
            JOptionPane.showMessageDialog(null, "Password Tidak Sama");
        }else if(pw1.equals("") || jabt.equals("") || nama.equals("")){
            JOptionPane.showMessageDialog(null, "Coba Isi Kembali");
        }else{
            try{
                    String sql = "INSERT INTO login_sipro(id_kamu, nama, jabatan, password) VALUES (" + "'" + tx_id.getText() + "','" + tx_nama.getText() + "','" 
            + tx_jabatan.getText() + "','" + tx_pw.getText() + "')";
                    java.sql.Connection conn=(java.sql.Connection)project.Koneksi.getConnection();
                    java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Berhasil disimpan");
                }
                catch (Exception e){
                    JOptionPane.showMessageDialog(null, "Gagal disimpan");
                    System.out.println(e.getMessage());
                }
        }
    }//GEN-LAST:event_btn_regMouseClicked

    private void btn_login1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_login1MouseClicked
        // TODO add your handling code here:
        if(tx_id1.equals("") || tx_nama1.equals("") || tx_pwlogin.equals("")){
            JOptionPane.showMessageDialog(null, "Terdapat kesalahan, harap ulangi kembali");
        }else{
            try{
            Connection c = Koneksi.getConnection();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM login_sipro WHERE id_kamu='" + tx_id1.getText()
                    +"' and password='"+ tx_pwlogin.getText() + "'";
            ResultSet r = s.executeQuery(sql);
            
            if (r.next()){
                JOptionPane.showMessageDialog(null, "Login Berhasil");
                this.dispose();
                Project1 a = new Project1();
                a.setVisible(true); //close the form
                
            }else{
                JOptionPane.showMessageDialog(null, "Terdapat kesalahan, harap ulangi kembali");
                tx_pwlogin.requestFocus();
            }
        }catch(SQLException e){
            System.out.println("error");
        }
        }
        
    }//GEN-LAST:event_btn_login1MouseClicked

