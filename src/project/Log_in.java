    private void idku(){
        try{
            Connection c = Koneksi.getConnection();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM login_sipro ORDER BY id DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoID = r.getString("id").substring(0);
                String ID = "" +(Integer.parseInt(NoID)+1);
                String Zero = "";
                
                if (ID.length()== 1) 
                {Zero = "010";}
                else if(ID.length()==2)
                {Zero = "01";}
                else if(ID.length()==3)
                {Zero = "0";}
                
                tx_id.setText("SIPRO_" + Zero + ID);
            }else{
                tx_id.setText("ADMIN_SIPRO-0101");
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }
